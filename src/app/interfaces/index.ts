/**
 * Setup instance of Account
 */
export interface Account {
    /**
   * Setup value of createdAt of timestamp format
   */
    createdAt: string

    /**
   * Setup value of name e.g. "Enzo Mora"
   */
    name: string

    /**
     * Setup value of avatar of File URL format
     */
    avatar: string

    /**
     * Setup value of password 
     */
    password: string

    /**
     * Setup value of email e.g. "sample@gmail.com"
     */
    email: string

    /**
     * Setup value of id e.g. "12"
     */
    id: string
  }
  